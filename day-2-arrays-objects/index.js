// --------------------
// Завдання 2: Масив обʼєктів
// --------------------

const users = [
    { name: 'Pavlo', age: 28 },
    { name: 'Petro', age: 17 },
    { name: 'Anna', age: 30 }
];

// Вивести імена користувачів старших 18 років
for (let i = 0; i < users.length; i++) {
    if (users[i].age > 18) {
        console.log(users[i].name);
    }
}
